    private void jComboBox1İtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1İtemStateChanged
        if(jComboBox1.getSelectedItem()==null)
        {
            ID=-1;
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
        else
        {
            Object item = jComboBox1.getSelectedItem();
            String value = ((ComboItem)item).getValue();
            ID = Integer.parseInt(value);
            
            for(ArrayList ders:dersler)
            {
                if (ders.get(0).toString().equals(value))
                {
                    jTextField1.setText(ders.get(1).toString());

                    ComboItem items;
                    for (int i = 0; i < jComboBox3.getItemCount(); i++)
                    {
                        items = (ComboItem)jComboBox3.getItemAt(i);
                        if (items.getValue().equalsIgnoreCase(ders.get(7).toString()))
                        {
                            jComboBox3.setSelectedIndex(i);
                            break;
                        }
                    }
                    
                    for (int i = 0; i < jComboBox2.getItemCount(); i++)
                    {
                        items = (ComboItem)jComboBox2.getItemAt(i);
                        if (items.getValue().equalsIgnoreCase(ders.get(8).toString()))
                        {
                            jComboBox2.setSelectedIndex(i);
                            break;
                        }
                    }
                    jTextField2.setText(ders.get(4).toString());
                    jTextField3.setText(ders.get(5).toString());
                    jTextField4.setText(ders.get(6).toString());
                }
            }
        }
    }//GEN-LAST:event_jComboBox1İtemStateChanged

