    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String dersAdi = jTextField1.getText().toString();
        String deger = jTextField2.getText().toString();
        String deger2 = jTextField3.getText().toString();
        String deger3 = jTextField4.getText().toString();
        if("".equals(dersAdi) || "".equals(deger) || "".equals(deger2) || "".equals(deger3) || ID2==-1 || ID3==-1)
        {
            System.out.println("Boş alanları doldurmadan dersi ekleyemezsiniz");
        }
        else
        {
            int sinif = Integer.parseInt(deger);
            float vizeD= Float.parseFloat(deger2), finalD=Float.parseFloat(deger3);
            boolean tekrar=false;
            for(ArrayList ders:dersler)
            {
                Object item = jComboBox2.getSelectedItem();
                String value = ((ComboItem)item).getValue();
                
                Object item2 = jComboBox2.getSelectedItem();
                String value2 = ((ComboItem)item).getValue();
                
                if(ders.get(1).toString().equals(dersAdi) 
                        && value2.equals(ders.get(7).toString()) 
                        && value.equals(ders.get(8).toString()))
                {
                    tekrar=true;
                }
            }
            if(!tekrar)
            {
                //String dersAdi, int fakulte, int bolum, int sinif, float vizeD, float finalD
                ArrayList veri = new ArrayList();
                veri.add(dersAdi);
                veri.add(ID2);
                veri.add(ID3);
                veri.add(sinif);
                veri.add(vizeD);
                veri.add(finalD);
                derslerIslemleri.Ekle(veri);
                ComboBox1Yenile();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jComboBox2.setSelectedIndex(-1);
                jComboBox3.setSelectedIndex(-1);
            }
            else
            {
                System.out.println("Girdiğiniz bilgiler zaten mevcut..");
            }
            
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

